#include <iostream>
#include <vector>

using namespace std;


const int64_t MOD = 998244353;      // модуль, по которому производим все вычисления


int64_t get_number(vector<int64_t> const &v, size_t i) {
    return i >= v.size() ? 0 : v[i];
}

vector<int64_t> sum(vector<int64_t> const &p, vector<int64_t> const &q) {
    vector<int64_t> res;

    int64_t temp;
    for (size_t i = 0; i < max(p.size(), q.size()); ++i) {
        temp = get_number(p, i) + get_number(q, i);
        temp %= MOD;
        if (temp < 0) {
            temp += MOD;
        }
        res.push_back(temp);
    }

    return res;
}

vector<int64_t> product(vector<int64_t> const &p, vector<int64_t> const &q) {
    vector<int64_t> res;

    int64_t temp;
    for (size_t i = 0; i <= p.size() + q.size() - 2; ++i) {
        temp = 0;
        for (size_t j = 0; j <= i; ++j) {
            temp = (temp + get_number(p, j) * get_number(q, i - j)) % MOD;
            if (temp < 0) {
                temp += MOD;
            }
        }
        res.push_back(temp);
    }

    return res;
}

vector<int64_t> division(vector<int64_t> const &p, vector<int64_t> const &q) {
    vector<int64_t> res;

    vector<int64_t> r = p;

    int64_t cur_pos = 0;
    while (res.size() < 1000) {
        if (r.size() <= cur_pos) {
            r.push_back(0);
        }
        int64_t next_res =
                r[cur_pos] % MOD;     // 'cause q[0] = 1 (that is why ans is vector<int64_t> isn't the vector<double>)
        if (next_res < 0) {
            next_res += MOD;
        }
        res.push_back(next_res);

        for (int64_t i = cur_pos; i < cur_pos + q.size(); ++i) {
            if (r.size() <= i) {
                r.push_back(0);
            }
            r[i] -= (q[i - cur_pos] * next_res) % MOD;
            r[i] %= MOD;
            if (r[i] < 0) {
                r[i] += MOD;
            }
        }

        cur_pos++;
    }

    return res;
}

void out_vector(vector<int64_t> const &v, bool out_degree) {
    int degree = v.size() - 1;
    while (degree > 0 && v[degree] == 0) {
        degree--;
    }
    if (degree == -1) {
        cout << 0 << std::endl;
    } else {

        if (out_degree)
            cout << degree << std::endl;
        else
            degree = 999;

        for (size_t i = 0; i <= degree; ++i) {
            cout << v[i] << ' ';
        }

        cout << std::endl;
    }
}

int main() {
    ios::sync_with_stdio(false);

    size_t n, m;
    cin >> n >> m;

    vector<int64_t> p(n + 1);
    for (size_t i = 0; i < n + 1; ++i) {
        cin >> p[i];
    }

    vector<int64_t> q(m + 1);
    for (size_t i = 0; i < m + 1; ++i) {
        cin >> q[i];
    }

    out_vector(sum(p, q), true);
    out_vector(product(p, q), true);
    out_vector(division(p, q), false);

    return 0;
}

